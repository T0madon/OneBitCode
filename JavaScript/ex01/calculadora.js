let num1 = parseFloat(prompt("Insira um valor: "));
let num2 = parseFloat(prompt("Insira o segundo valor: "));

alert(num1 + " + " + num2 + " = " + (num1 + num2));
alert(num1 + " - " + num2 + " = " + (num1 - num2));
alert(num1 + " * " + num2 + " = " + num1 * num2);
alert(num1 + " / " + num2 + " = " + num1 / num2);
